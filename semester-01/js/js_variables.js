//javascript variables
//there are for ways to declare variables in js

//automatically
x = 5;
y = 6;
z = x + y;
console.log(z); //prints 11

//end

//using var keyword
var x = 5;
var y = 6;
var z = x + y;
console.log(z); //prints 11

//end

//usint let keyword
let a = 5;
let b = 6;
let c = a + b;
console.log(c); //prints 11

//end

//using const keyword
const i = 10;
const j = 12;
const k = i + j;
console.log(k); //prints 22

//end
